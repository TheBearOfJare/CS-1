public class Duck {
    private int volume;
    private int height;
    private int weight;
    private String colour;
    private String name;


    public Duck(int volume, int height, int weight, String colour, String name) {
        this.volume =  volume;
        this.height = height;
        this.weight = weight;
        this.colour = colour;
        this.name = name;
    }
}